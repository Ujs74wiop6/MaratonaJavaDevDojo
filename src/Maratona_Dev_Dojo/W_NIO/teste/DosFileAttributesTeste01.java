package Maratona_Dev_Dojo.W_NIO.teste;

import java.io.File;
import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.nio.file.attribute.DosFileAttributeView;
import java.nio.file.attribute.DosFileAttributes;

/**
 *
 * @author Fabricio
 */
public class DosFileAttributesTeste01 {

    public static void main(String[] args) throws IOException {
        Path path = Paths.get("C:\\Users\\Home\\Documents\\NetBeansProjects\\MaratonaJavaDevDojo\\pasta\\teste.txt");
        if (Files.notExists(path)) 
            Files.createFile(path);
        DosFileAttributes dosFileAttributes = Files.readAttributes(path, DosFileAttributes.class);
        System.out.println(dosFileAttributes.isHidden());
        System.out.println(dosFileAttributes.isReadOnly());

        System.out.println("------------------------");

        DosFileAttributeView fileAttributeView = Files.getFileAttributeView(path, DosFileAttributeView.class);
        fileAttributeView.setHidden(true);
        fileAttributeView.setReadOnly(true);
        System.out.println(fileAttributeView.readAttributes().isHidden());
        System.out.println(fileAttributeView.readAttributes().isReadOnly());
    }
}
